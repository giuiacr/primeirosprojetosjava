package iniciante;

public class IfElse {
	public  static void main(String[] args) {
		
		int x = 8;
		
		if(x > 10) {
			System.out.println("x é maior que 10");
		} else if(x == 10) {
			System.out.println("x é igual a 10");
		} else {
			System.out.println("x é menor que 10");
		}
	}
}
